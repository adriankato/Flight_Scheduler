    private void cancelCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelCustomerButtonActionPerformed
        // TODO add your handling code here:
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        BookingQueries bookingQueries = new BookingQueries();
        Waitlist waitlist = new Waitlist();
        bookingQueries.cancelBooking((String) cancelCustomerCustomerComboBox.getSelectedItem(), (Date) cancelCustomerDayComboBox.getSelectedItem());
        cancelCustomerResults.setText((String) cancelCustomerCustomerComboBox.getSelectedItem() + "'s booking on " + cancelCustomerDayComboBox.getSelectedItem() + " has been cancelled. \n");
        if ((waitlist.statusByDay((Date) cancelCustomerDayComboBox.getSelectedItem())) != "Name" + "\t" + "Flight" + "\t\t" + "Timestamp" + "\n") {
            BookingEntry copyEntry = new BookingEntry();
            copyEntry = waitlist.getWaitlist((Date) cancelCustomerDayComboBox.getSelectedItem());

            bookingQueries.addBooking(waitlist.getWaitlist((Date) cancelCustomerDayComboBox.getSelectedItem()), currentTimestamp);
            cancelCustomerResults.append(copyEntry.getCustomer() + " has been booked a flight on " + copyEntry.getFlight() + " from the waitlist. \n");
            waitlist.cancelWaitlistCustomerDay(waitlist.getWaitlist((Date) cancelCustomerDayComboBox.getSelectedItem()));
            cancelCustomerResults.append(copyEntry.getCustomer() + " has been removed from the waitlist on " + copyEntry.getDay() + ".");
        }
    }//GEN-LAST:event_cancelCustomerButtonActionPerformed

    private void dropFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropFlightButtonActionPerformed
        // TODO add your handling code here:
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        BookingQueries bookingQueries = new BookingQueries();
        Waitlist waitlist = new Waitlist();

        //String waitlistStatus = waitlist.statusByFlight((String)dropFlightFlightComboBox.getSelectedItem());
        //String bookingStatus = bookingQueries.statusByFlight((String)dropFlightFlightComboBox.getSelectedItem());
        Flight.cancelFlight((String) dropFlightFlightComboBox.getSelectedItem());
        dropFlightResults.setText("Flight " + dropFlightFlightComboBox.getSelectedItem().toString() + " has been cancelled. \n");

        while (waitlist.statusByFlight((String) dropFlightFlightComboBox.getSelectedItem()) != "Name" + "\t" + "Flight" + "\t\t" + "Timestamp" + "\n") {
            BookingEntry bookingEntry = new BookingEntry();
            bookingEntry = waitlist.getWaitlistbyFlight((String) dropFlightFlightComboBox.getSelectedItem());
            waitlist.cancelWaitlistFlight(waitlist.getWaitlistbyFlight((String) dropFlightFlightComboBox.getSelectedItem()));
            dropFlightResults.append(bookingEntry.getCustomer() + " has been removed from the waitlist for flight " + bookingEntry.getFlight() + ". \n");
        }

        while (bookingQueries.statusByFlight((String) dropFlightFlightComboBox.getSelectedItem()) != "Name" + "\t\t" + "Timestamp" + "\n") {
            BookingEntry bookingToBeDeleted = new BookingEntry();
            BookingEntry vacantBooking = new BookingEntry();
            String emptyFlight;

            ArrayList<BookingEntry> vacantBookingList = new ArrayList<BookingEntry>(bookingQueries.getVacantBooking(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem())));
            bookingToBeDeleted = bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem());
            ArrayList<String> emptyFlightsList = new ArrayList(bookingQueries.getEmptyFlights(bookingToBeDeleted.getFlight(), bookingToBeDeleted.getDay()));

            if (!(vacantBookingList.isEmpty())) {
                vacantBooking = vacantBookingList.get(0);
                bookingQueries.addRebooking(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem()), vacantBooking, currentTimestamp);
                dropFlightResults.append(bookingToBeDeleted.getCustomer() + " has been rebooked to flight " + vacantBooking.getFlight() + " on " + vacantBooking.getDay() + ". \n");
                bookingQueries.cancelBookingByEntry(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem()));
                dropFlightResults.append(bookingToBeDeleted.getCustomer() + "'s flight " + bookingToBeDeleted.getFlight() + " on " + bookingToBeDeleted.getDay() + " has been cancelled. \n");

            } else if (!(emptyFlightsList.isEmpty())) {
                emptyFlight = emptyFlightsList.get(0);
                bookingQueries.bookEmptyFlights(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem()), emptyFlight, currentTimestamp);
                dropFlightResults.append(bookingToBeDeleted.getCustomer() + " has been rebooked to flight " + emptyFlight + " on " + bookingToBeDeleted.getDay() + ". \n");
                bookingQueries.cancelBookingByEntry(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem()));
                dropFlightResults.append(bookingToBeDeleted.getCustomer() + "'s flight " + bookingToBeDeleted.getFlight() + " on " + bookingToBeDeleted.getDay() + " has been cancelled. \n");

            } else {
                bookingQueries.cancelBookingByEntry(bookingQueries.getBookingByFlight((String) dropFlightFlightComboBox.getSelectedItem()));
                dropFlightResults.append(bookingToBeDeleted.getCustomer() + "'s flight " + bookingToBeDeleted.getFlight() + " on " + bookingToBeDeleted.getDay() + " has been cancelled. \n");
            }
        }

        bookingFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(Flight.getFlightNames().toArray()));
        flightStatusFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(Flight.getFlightNames().toArray()));
        dropFlightFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(Flight.getFlightNames().toArray()));
    }//GEN-LAST:event_dropFlightButtonActionPerformed

