    private void bookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookingButtonActionPerformed
        // TODO add your handling code here:
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        BookingEntry bookingEntry = new BookingEntry((String) bookingCustomerComboBox.getSelectedItem(),
                (String) bookingFlightComboBox.getSelectedItem(), (Date) bookingDayComboBox.getSelectedItem(),
                currentTimestamp);

        int numberOfSeats = Flight.getFlightSeats(bookingEntry.getFlight(), bookingEntry.getDay());

        if (numberOfSeats < Flight.getMaxSeatCount(bookingEntry.getFlight())) {
            BookingQueries bookingQueries = new BookingQueries();

            int result = bookingQueries.addBooking(bookingEntry, bookingEntry.getTimestamp());
            if (result == 1) {

                bookingResults.setText(bookingEntry.getCustomer() + " has been booked flight " + bookingEntry.getFlight() + " on " + bookingEntry.getDay() + ".");

            }
        } else {
            Waitlist waitlist = new Waitlist();

            int result = waitlist.addWaitlist(bookingEntry, bookingEntry.getTimestamp());

            if (result == 1) {
                bookingResults.setText(bookingEntry.getCustomer() + " has been waitlisted for a flight on " + bookingEntry.getDay() + ".");

            }
        }

    }//GEN-LAST:event_bookingButtonActionPerformed

